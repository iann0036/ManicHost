                                            <h4 class="panel-title">
                                                <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion4" href="#accordion4_1">
                                                    What is a Domain Validation certificate? </a>
                                            </h4>
                                        </div>
                                        <div id="accordion4_1" class="panel-collapse collapse in">
                                            <div class="panel-body">
                                                <p>
                                                    To get domain validated certificate applicant would need to prove to Certification Authority that he has control over domain for which the certificate is requested. Certification authority will send approval email either to one of generic email addresses, e.g. admin@ webmaster@ and some other, associated with the domain or to email address from domain WhoIs contacts. If applicant is able to receive that email and approve it, then Certification Authority would issue the certificate. This validation procedure is used to ensure internet users that domain holders are who they claim to be. Domain validated certificate will have no information about applicant's company, it will have only domain name for which it has been issued. Here is what you will see in Page Info of site secured by domain validated certificate:
                                                </p>
                                                <p>
                                                    <b>Web site:</b> www.yourdomain.com<br />
                                                    <b>Owner:</b> This web-site does not supply ownership information.<br />
                                                    <b>Verified by:</b> Name of Certification Authority<br />
                                                </p>
                                            </div>
                                        </div>
                                    </div>
                                    <div class="panel panel-default">
                                        <div class="panel-heading">
                                            <h4 class="panel-title">
                                                <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion4" href="#accordion4_2">
                                                    What is an Organization Validation certificate? </a>
                                            </h4>
                                        </div>
                                        <div id="accordion4_2" class="panel-collapse collapse">
                                            <div class="panel-body">
                                                <p>
                                                   During validation of order for organization validated certificate Certification Authority will ask applicant to provide proof that his company is registered and legally accountable business. Certification Authority would also require domain contacts in WhoIs to match contact details of applicant's company. Organization validated certificate will have domain and company name of applicant. Page Info of site secured by organization validated certificate will look like following:
                                                </p>
                                                <p>
                                                   <b>Web site:</b> www.yourdomain.com<br />
                                                   <b>Owner:</b> Your Company Name<br />
                                                   <b>Verified by:</b> Name of Certification Authority
                                               </p>
                                            </div>
                                        </div>
                                    </div>
