                                            <h4 class="panel-title">
                                                <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion4" href="#accordion4_3">
                                                    What is an Extended Validation certificate? </a>
                                            </h4>
                                        </div>
                                        <div id="accordion4_3" class="panel-collapse collapse">
                                            <div class="panel-body">
                                                <p>
                                                    To validate order for certificate with extended validation all Certification Authorities will follow the same detailed issuance requirements which aim to:
                                                </p>
                                                <p>
                                                <ul>
                                                    <li>Establish the legal identity as well as the operational and physical presence of website owner;</li>
                                                    <li>Establish that the applicant is the domain name owner or has exclusive control over the domain name; and</li>
                                                    <li>Confirm the identity and authority of the individuals acting for the website owner, and that documents pertaining to legal obligations are signed by an authorized officer.</li>
                                                </ul>
                                                </p>
                                                <p>
                                                    Only certificates with extended validation will trigger the green bar in high-security web browsers.</p>
                                                </p>
